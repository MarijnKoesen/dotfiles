# Set default editor (for git and many other binaries)
export EDITOR='vim'

# Add 256 color support for your TERM
export TERM=screen-256color

# Turn on colors for commands like `ls`
export CLICOLOR=1

# Make my shell colors like ubuntu
export LSCOLORS=ExGxBxDxCxEgEdxbxgxcxd

# Fix loggin in to digital ocean machines
export LC_ALL="en_US.UTF-8"

# Show time a command took if over 4 sec
export REPORTTIME=4
export TIMEFMT="%*Es total, %U user, %S system, %P cpu"
